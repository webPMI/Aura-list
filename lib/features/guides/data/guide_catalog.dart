/// Devuelve el guía con [id] o null si no existe.
Guide? getGuideById(String id) {
  try {
    return kGuideCatalog.firstWhere((g) => g.id == id);
  } catch (_) {
    return null;
  }
}

/// IDs de todos los guías disponibles (para selector y validación).
List<String> get availableGuideIds =>
    kGuideCatalog.map((g) => g.id).toList();
